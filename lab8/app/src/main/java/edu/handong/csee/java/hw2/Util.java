package edu.handong.csee.java.hw2;
/**
 * this class make every input upper.
 */
public class Util {
    /**
     * this method make all inputs to uppercase
     * @param stringValue input value
     * @return uppercase
     */
    static String getUppercaseString(String stringValue) {
        return stringValue.toUpperCase();
    }
    
}
